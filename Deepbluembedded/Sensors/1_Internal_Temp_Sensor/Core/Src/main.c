/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{


  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_TIM3_Init();
  MX_USART1_UART_Init();

  HAL_TIM_Base_Start(&htim3); // Start Timer3 (Trigger Source For ADC1)
  HAL_ADCEx_Calibration_Start(&hadc1);
  HAL_ADC_Start_DMA(&hadc1, (uint32_t*)AD_RES, 2);   // Start ADC Conversion

  while (1)
  {
      if(UpdateEvent){
          // Calculate The Temperature
          V_Ref = (float)((V_REF_INT * 4095.0)/AD_RES[0]);
          V_Sense = (float)(AD_RES[1] * V_Ref) / 4095.0;
          Temperature = (((V_AT_25C - V_Sense) * 1000.0) /AVG_SLOPE) + 25.0;
          // Send The Result To PC Over Serial Port
          sprintf(TxBuffer, "%f\r\n", Temperature);
          HAL_UART_Transmit(&huart1, (uint8_t*)TxBuffer, strlen(TxBuffer), 100);
          UpdateEvent = 0;
      }
  }

}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
    HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_0); // Toggle Interrupt Rate Indicator Pin
    UpdateEvent = 1;
}



